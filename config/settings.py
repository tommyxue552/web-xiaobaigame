# config/settings.py
# ==================
# 采集器全局配置。
# 所有可调参数集中在此文件，便于部署时修改。

from __future__ import annotations

import os
from pathlib import Path

# ---------------------------------------------------------------------------
# 项目路径
# ---------------------------------------------------------------------------

PROJECT_ROOT = Path(__file__).resolve().parent.parent

# ---------------------------------------------------------------------------
# 定时采集
# ---------------------------------------------------------------------------

# 每天自动采集的时间（24 小时制，HH:MM）
COLLECT_SCHEDULE_TIME: str = os.environ.get("COLLECT_SCHEDULE_TIME", "03:00")

# 调度器时区（None 表示使用系统本地时区）
SCHEDULER_TIMEZONE: str | None = os.environ.get("SCHEDULER_TIMEZONE") or None

# ---------------------------------------------------------------------------
# 站点配置
# ---------------------------------------------------------------------------

SITES_CONFIG_PATH: Path = PROJECT_ROOT / "crawler" / "sites.yaml"

# ---------------------------------------------------------------------------
# 运行记录
# ---------------------------------------------------------------------------

RUN_RECORDS_DIR: Path = PROJECT_ROOT / "logs" / "run_records"

# ---------------------------------------------------------------------------
# 日志
# ---------------------------------------------------------------------------

LOG_DIR: str = str(PROJECT_ROOT / "logs")

# ---------------------------------------------------------------------------
# 上传默认值
# ---------------------------------------------------------------------------

DEFAULT_UPLOAD_URL: str = os.environ.get("UPLOAD_URL", "http://localhost:8000")
DEFAULT_UPLOAD_LIMIT: int = int(os.environ.get("UPLOAD_LIMIT", "0"))  # 0 = 不限制
